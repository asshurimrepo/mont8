<?php

class ffColor extends ffBasicObject {

################################################################################
# CONSTANTS
################################################################################

################################################################################
# PRIVATE OBJECTS
################################################################################
	
################################################################################
# PRIVATE VARIABLES	
################################################################################	
	private $_hex = null;	
	
	private $_r = null;
	
	private $_g = null;
	
	private $_b = null;
	
	private $_alpha = null;
################################################################################
# CONSTRUCTOR
################################################################################	

################################################################################
# ACTIONS
################################################################################
	
################################################################################
# PUBLIC FUNCTIONS
################################################################################	
	public function setHex( $hex, $alpha = 1 ) {
		$this->_hex = $hex;
		$this->_alpha = $alpha;
	}
	
	public function setRgb( $r, $g, $b, $alpha = 1 ) {
		$this->_r = $r;
		$this->_g = $g;
		$this->_b = $b;
		$this->_alpha = $alpha;
	}
	
	public function getHex() {
		return $this->_hex;
	}
	
	public function getRgb() {
		return array('r' => $this->_r, 'g' => $this->_g, 'b' => $this->_b, 'a' => $this->_alpha );
	}
	
	public function getR() {
		return $this->_r;
	}
	
	public function getG() {
		return $this->_g;
	}
	
	public function getB() {
		return $this->_b;
	}
	
	public function getA() {
		return $this->_alpha;
	}
################################################################################
# PRIVATE FUNCTIONS
################################################################################
	 
################################################################################
# GETTERS AND SETTERS
################################################################################	
	
}